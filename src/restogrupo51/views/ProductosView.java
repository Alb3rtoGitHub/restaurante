    private void jtblProductosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblProductosMousePressed
        borrarDatos();
        if (jtblProductos.getSelectedRow() > -1) {
            String idString = jtblProductos.getValueAt(jtblProductos.getSelectedRow(), 0).toString();
            int idProducto = Integer.parseInt(idString);        
        
            Producto producto = new Producto();
            ProductoData productoData = new ProductoData();
            
            producto = productoData.buscarProducto(idProducto);
            if (producto != null) {
                jtxtId.setText(String.valueOf(producto.getIdProducto()));
                jtxtCodigo.setText(String.valueOf(producto.getCodigoProducto()));
                jtxtNombre.setText(String.valueOf(producto.getNombreProducto()));
                jtxtPrecio.setText(Double.toString(producto.getPrecio()));
                jtxtStock.setText(Integer.toString(producto.getStock()));
                
                if(producto.isEstadoProducto() == true){
                    jbtnEliminar.setText("Eliminar");
                }else{
                    jbtnEliminar.setText("Restaurar");
                }
            }
        }
    }//GEN-LAST:event_jtblProductosMousePressed

