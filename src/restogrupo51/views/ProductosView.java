    private void armarCabecera() {
        modelo.addColumn("ID");
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        modelo.addColumn("Stock");
        modelo.addColumn("Estado");
        jtblProductos.setModel(modelo);
    }

    private void borrarFilas() {
        int f = jtblProductos.getRowCount() - 1; 
        for (; f >= 0; f--) { 
            modelo.removeRow(f);
        }
    }
    
    private void cargarTabla(){
        String act = "";
        
        for (Producto producto : producto.listarProductos()) {
            if (producto.isEstadoProducto()) {
                act = "Activo";
            } else {
                act = "Eliminado";
            }
            modelo.addRow(new Object[]{producto.getIdProducto(), producto.getCodigoProducto(), producto.getNombreProducto(), producto.getPrecio(), producto.getStock(), act});
        }
    }
    
